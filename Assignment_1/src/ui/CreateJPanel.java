        String firstNameInput = firstName.getText();  
        String lastNameInput = lastName.getText();    
        String emailInput = email.getText();          
        String phoneInput = phone.getText();          
        String driversLicenseNumberInput = driverLicenseNumber.getText();  
        String driversLicenseStateInput = driverLicenseState.getText();    
        String socialSecurityNumberInput = socialSecurityNumber.getText();  
        String addressLine1Input = addressLine1.getText();   
        String addressLine2Input = addressLine2.getText();   

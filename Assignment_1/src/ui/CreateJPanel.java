        String occupationInput = occupation.getText();   
        String maritalStatusInput = martalStatus.getText();   
        String nationalityInput = nationality.getText();   
        String preferredLanguageInput = preferredLanguage.getText(); 
        
        person.setFirstName(firstNameInput);
        person.setLastName(lastNameInput);
        person.setEmail(emailInput);
        person.setPhone(phoneInput);
        person.setDriverLicenseNumber(driversLicenseNumberInput);
        person.setDriverLicenseState(driversLicenseStateInput);
        person.setSocialSecurityNumber(socialSecurityNumberInput);
        person.setAddressLine1(addressLine1Input);
        person.setAddressLine2(addressLine2Input);
        person.setCity(cityInput);
        person.setState(stateInput);
        person.setZip(zipInput);
        person.setTelephoneNumber(telephoneNumberInput);
        person.setFaxNumber(faxNumberInput);
        person.setDateofBirth(dateOfBirthInput);
        person.setGender(genderInput);
        person.setOccupation(occupationInput);
        person.setMartalStatus(maritalStatusInput);
        person.setNationality(nationalityInput);
        person.setPreferredLanguage(preferredLanguageInput);        
        
        JOptionPane.showMessageDialog(this, "Profile Created", "Success", JOptionPane.PLAIN_MESSAGE);
        
        firstName.setText("");
        lastName.setText("");
        email.setText("");
        phone.setText("");
